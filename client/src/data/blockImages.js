export function getBlockImage(blockName) {
  const key = blockName.toLowerCase().replace(/\s+/g, '_');
  return blockImages[key] || null;
}

export function getBlockDisplay(blockName) {
  const image = getBlockImage(blockName);
  const key = blockName.toLowerCase().replace(/\s+/g, '_');
  
  if (image) {
    return { type: 'image', url: image };
  }
  
  return { type: 'emoji', emoji: fallbackEmojis[key] || '📦' };
}
