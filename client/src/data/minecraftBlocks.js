// Keep the old emoji function as fallback for any components that still use it
export const getBlockIcon = (blockName) => {
  const display = getBlockDisplay(blockName);
  return display.type === 'emoji' ? display.emoji : '📦';
};

export const searchBlocks = (query) => {
  if (!query) return [];
  
  const lowerQuery = query.toLowerCase();
  return minecraftBlocks.filter(block => 
    block.name.toLowerCase().includes(lowerQuery) ||
    block.displayName.toLowerCase().includes(lowerQuery) ||
    block.category.toLowerCase().includes(lowerQuery)
  ).slice(0, 10);
};
